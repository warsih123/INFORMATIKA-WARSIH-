# Contoh penggunaan variabel dalam Python

# Menyimpan data ke dalam variabel
nama = "warsih"
umur = 16
tinggi = 167.5
mahasiswa = True

# Menampilkan isi variabel
print("Nama:", nama)
print("Umur:", umur)
print("Tinggi badan:", tinggi, "cm")
print("Status mahasiswa:", mahasiswa)

# Operasi dengan variabel
umur_tahun_depan = umur + 1
print("Umur tahun depan:", umur_tahun_depan)